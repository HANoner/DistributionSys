package com.qf.distributionsys.entity;

public class Punish {
    private Integer id;

    private Integer uid;

    private Integer award;

    private Integer punish;

    private Integer punishuid;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getUid() {
        return uid;
    }

    public void setUid(Integer uid) {
        this.uid = uid;
    }

    public Integer getAward() {
        return award;
    }

    public void setAward(Integer award) {
        this.award = award;
    }

    public Integer getPunish() {
        return punish;
    }

    public void setPunish(Integer punish) {
        this.punish = punish;
    }

    public Integer getPunishuid() {
        return punishuid;
    }

    public void setPunishuid(Integer punishuid) {
        this.punishuid = punishuid;
    }
}